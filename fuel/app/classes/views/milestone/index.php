</p>
<h3>
    Why is Terra?
</h3>
<p>
    Terra exists for the sole purpose of replacing all everyday products in our lives, such as ziploc bags and plastic wrap, with versions
    that are more friendly towards the environment. This is a simple way for everyday folks to be able to contribute to cleaning up our 
    planet. 
</p>
